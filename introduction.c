// #include is a pre-processor directive,
// it tells the preprocessor to include the "stdio.h" header
// to support input/output (I/O) operations.
#include <stdio.h>

int main(void) {                // main is the entry point of the program execution, it is required to return an int.
    printf("Hello, World!\n");  // says hello
    return 0;                   // terminate main()
}                               // end of main